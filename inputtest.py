num1 = input("숫자1 ==> ")
num1 = int(num1)

num2 = input("숫자2 ==> ")
num2 = int(num2)

numSum = num1 + num2
print(num1, '+', num2, '=', numSum)

numDiff = num1 - num2
print(num1, '-', num2, '=', numDiff)

numProd = num1 * num2
print(num1, '*', num2, '=', numProd)

numQuot = num1 / num2
print(num1, '/', num2, '=', numQuot)

numRem = num1 % num2
print(num1, '%', num2, '=', numRem)

numPow = num1 ** num2
print(num1, '**', num2, '=', numPow)