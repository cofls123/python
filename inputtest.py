num1 = input("숫자1 ==> ")
num1 = int(num1)

num2 = input("숫자2 ==> ")
num2 = int(num2)

#합
numSum = num1 + num2
print(num1, '+', num2, '=', numSum)

#차
numDiff = num1 - num2
print(num1, '-', num2, '=', numDiff)

#곱
numProd = num1 * num2
print(num1, '*', num2, '=', numProd)

#몫
numQuot = num1 / num2
print(num1, '/', num2, '=', numQuot)

#나머지
numRem = num1 % num2
print(num1, '%', num2, '=', numRem)

#거듭제곱
numPow = num1 ** num2
print(num1, '**', num2, '=', numPow)